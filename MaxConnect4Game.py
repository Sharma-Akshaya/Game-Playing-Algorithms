#MinMax Class (using Pseudocode discussed in the class)
class Minimax:
	def __init__(self, game, depth):
		self.currentTurn = game.currentTurn
		self.game = game
		self.maxDepth = int(depth)

#Function to compute the result of minmax
	def ComputeStep(self): 
		minValues = []
		possMoves = successor(self.game.gameBoard)
		for move in possMoves:
			rslt = result(self.game,move)
			minValues.append( self.MinimalV(rslt,99999,-99999) )
		chosen = possMoves[minValues.index( max( minValues ) )]
		return chosen

#Min Value Function of MinMax Algorithm (using Pseudocode discussed in the class)
	def MinimalV(self, state, alpha, beta):  # minimizing operations
		if state.pieceCount == 42 or state.topDown == self.maxDepth:
			return self.Top_Util(state)
		val = 99999
		for move in successor(state.gameBoard):
			newState = result(state,move)
			val = min(val,self.maximumResult( newState,alpha,beta ))
			if val <= alpha:
				return val
			beta = min(beta, val)
		return val

#Max Value Function of MinMax Algorithm (using Pseudocode discussed in the class)
	def maximumResult(self, state, alpha, beta):  #this function executes the maximizing operations
		if state.pieceCount == 42 or state.topDown == self.maxDepth:
			return self.Top_Util(state)
		val = -99999
		for move in successor(state.gameBoard):
			newState = result(state,move)
			val = max(val,self.MinimalV( newState,alpha,beta ))
			if val >= beta:
				return val
			alpha = max(alpha, val)
		return val

#Utility Function of MinMax Algorithm (using Pseudocode discussed in the class)
	def Top_Util(self,state):
		if self.currentTurn == 1:
			Top_Util = state.PlayerCount * 2 - state.ComputerScore
		elif self.currentTurn == 2:
			Top_Util = state.ComputerScore * 2 - state.PlayerCount
		return Top_Util

#Successor Function to play the next move (using Pseudocode discussed in the class)
def successor(board):
    successor = []
    for col, colVal in enumerate(board[0]):
        if colVal == 0:
            successor.append(col)
    return successor

#Result of the game played
def result(oldGame,column):
    newGame=maxConnect4Game()
    try:
        newGame.topDown = oldGame.topDown + 1
    except AttributeError:
        newGame.topDown = 1
    newGame.pieceCount=oldGame.pieceCount
    newGame.gameBoard = copy.deepcopy(oldGame.gameBoard)
    if not newGame.gameBoard[0][column]:
        for i in range(5,-1,-1):
            if not newGame.gameBoard[i][column]:
                newGame.gameBoard[i][column]=oldGame.currentTurn
                newGame.pieceCount+=1
                break
    if oldGame.currentTurn ==1:
        newGame.currentTurn = 2
    elif oldGame.currentTurn ==2:
        newGame.currentTurn = 1
    newGame.checkPieceCount()
    newGame.countScore()
    return newGame


